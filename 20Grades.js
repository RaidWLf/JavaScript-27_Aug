let grade = 50;

if (100 >= grade && grade >= 80) {
  console.log("Grade of Student is A");
} else if (89 >= grade && grade >= 70) {
  console.log("Grade of Student is B");
} else if (69 >= grade && grade >= 60) {
  console.log("Grade of Student is C");
} else if (59 >= grade && grade >= 50) {
  console.log("Grade of Student is D");
} else if (49 >= grade && grade >= 0) {
  console.log("Grade of Student is F");
} else {
  console.log("Please input Grade between 0 - 100");
}
